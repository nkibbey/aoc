
def opparse(arr, input=-1)
    arr_length = arr.length
    op_index = 0

    while op_index < arr_length && arr[op_index] != 99
        op_type = arr[op_index].to_s

        # if op_type[-1] == "1" || op_type[-1] == "2" || op_type[-1] == "5" || op_type[-1] == "6" || op_type[-1] == "7" || op_type[-1] == "8"
        param1 = arr[op_index + 1]
        param2 = arr[op_index + 2]
        param3 = arr[op_index + 3]

        val1 = arr[param1]
        val2 = arr[param2]
        result_index = param3 # standard when doing +=4

        if op_type.length >= 3 && op_type[-3] == "1"
            val1 = param1
        end
    
        if op_type.length >= 4 && op_type[-4] == "1"
            val2 = param2
        end
        
        if op_type.length >= 5 && op_type[-5] == "1"
            puts "I got that thing you didnt expect"
        end

        if op_type[-1] == "1"     # add
            arr[result_index] = val1 + val2
            op_index += 4
        elsif op_type[-1] == "2"  # multiply
            arr[result_index] = val1 * val2
            op_index += 4
        elsif op_type[-1] == "3" # input
            result_index      = arr[op_index + 1]
            arr[result_index] = input
            op_index += 2
        elsif op_type[-1] == "4" # output
            puts "answer " +val1.to_s
            op_index += 2
        elsif op_type[-1] == "5"  # if true then 
            if val1 != 0
                op_index = val2
            else
                op_index += 3
            end
        elsif op_type[-1] == "6"   # if false then
            if val1 == 0
                op_index = val2
            else
                op_index += 3
            end
        elsif op_type[-1] == "7"   # less than
            res = 0
            if val1 < val2 
                res = 1
            end
            arr[result_index] = res
            op_index += 4            
        elsif op_type[-1] == "8"   # equals
            res = 0
            if val1 == val2 
                res = 1
            end
            arr[result_index] = res
            op_index += 4
        else
            puts 'no bueno ' + op_index.to_s + ' ' + op_type.to_s 
            op_index += arr_length
        end
    end    
end

